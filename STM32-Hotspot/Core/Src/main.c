/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  MX_SPI2_Init();
  MX_I2C2_Init();
  MX_ADC_Init();
  MX_LPTIM1_Init();
  MX_TIM2_Init();
  MX_LPUART1_UART_Init();
  /* USER CODE BEGIN 2 */

  /**
   * LoRa
   */
  init_rn2483(&hlpuart1);
  //UART_HandleTypeDef hlpuart1;

  /**
   * Anemometer
   */
  Anemometer_init(&hadc);

  HAL_Delay(100);

  /**
   * BME680
   */
  bme680_interface_init(&hi2c2);

  HAL_Delay(1000);

  /**
   * MLX90640
   */
  MLX90640_I2CInit(&hi2c2);

  MLX90640_SetRefreshRate(MLX90640_ADDR, RefreshRate);
  MLX90640_SetChessMode(MLX90640_ADDR);

  paramsMLX90640 mlx90640;
  MLX90640_DumpEE(MLX90640_ADDR, eeMLX90640); //if (status != 0) printf("\r\nload system parameters error with code:%d\r\n",status);
  MLX90640_ExtractParameters(eeMLX90640, &mlx90640);

  HAL_Delay(100);

  /**
   * PT100
   */
  SPIDevice sensor1;
  sensor1.hspi = &hspi2;
  sensor1.gpio = GPIOA;
  sensor1.pin = GPIO_PIN_9;

  SPIDevice sensor2;
  sensor2.hspi = &hspi2;
  sensor2.gpio = GPIOA;
  sensor2.pin = GPIO_PIN_0;

  SPIDevice sensor3;
  sensor3.hspi = &hspi2;
  sensor3.gpio = GPIOB;
  sensor3.pin = GPIO_PIN_10;


  if(Init_Ada(sensor1)){
	  uint8_t message[16] = "1: Init problem\n";
	  HAL_UART_Transmit(&huart1, message, 16, 100);
  }

  if(Init_Ada(sensor2)){
  	  uint8_t message[16] = "2: Init problem\n";
  	  HAL_UART_Transmit(&huart1, message, 16, 100);
  }

  if(Init_Ada(sensor3)){
  	  uint8_t message[16] = "3: Init problem\n";
  	  HAL_UART_Transmit(&huart1, message, 16, 100);
  }

  HAL_Delay(100);



  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  /**
	   * Anemometer
	   */

	  double wind = getWindSpeed();
	  displayTemp(huart1,wind);

	  /**
	   * PT100
	   */
	  double temp1 = Get_Temperature(sensor1);

	  uint8_t message1[10] = "Sensor 1: ";
	  HAL_UART_Transmit(&huart1, message1, 10, 100);
	  displayTemp(huart1,temp1);

	  double temp2 = Get_Temperature(sensor2);

	  uint8_t message2[10] = "Sensor 2: ";
	  HAL_UART_Transmit(&huart1, message2, 10, 100);
	  displayTemp(huart1,temp2);

	  double temp3 = Get_Temperature(sensor3);

	  uint8_t message3[10] = "Sensor 3: ";
	  HAL_UART_Transmit(&huart1, message3, 10, 100);
	  displayTemp(huart1,temp3);

	  /**
	   * MLX90640
	   */
	  MLX90640_GetFrameData(MLX90640_ADDR, frame);

	  //float vdd = MLX90640_GetVdd(frame, &mlx90640);
	  float Ta = MLX90640_GetTa(frame, &mlx90640);

	  float tr = Ta - TA_SHIFT; //Reflected temperature based on the sensor ambient temperature

	  MLX90640_CalculateTo(frame, &mlx90640, emissivity , tr, mlx90640To);

	  double result = 0.0;
	  int nbTemps = 1;

	  for(int i = 0; i < 768; i++){
		  if(mlx90640To[i] != 0.0){
			  result += mlx90640To[i];
			  nbTemps++;
		  }
	  }

	  result = result/nbTemps;
	  displayTemp(huart1,result);


	  /**
	   * BME680
	   */
	  float temperature = 0.0;
	  float humidity = 0.0;
	  getTemperatureHumidity(&temperature, &humidity);
	  printTemperatureHumidity(temperature, humidity, &huart1);



	  /**
	   * LoRa
	   */
	  send_rn2483(&hlpuart1, 1, "", 0);


	  HAL_Delay(10000);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */
}

